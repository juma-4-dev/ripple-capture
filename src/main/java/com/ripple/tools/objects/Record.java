package com.ripple.tools.objects;

public class Record {

	private Long time;

	private Integer increment;

	public Record() {

	}

	public Long getTime() {
		return time;
	}

	public void setTime(Long time) {
		this.time = time;
	}

	public Integer getIncrement() {
		return increment;
	}

	public void setIncrement(Integer increment) {
		this.increment = increment;
	}
}
